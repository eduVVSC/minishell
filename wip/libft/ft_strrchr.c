#include "libft.h"

char	*ft_strrchr(const char *s, int c)

{
	int	len;

	len = (int)ft_strlen(s);
	if (((unsigned char)c) == 0)
		return ((char *)&s[len]);
	while (len >= 0)
	{
		if (s[len] == (unsigned char)c)
			return ((char *)&s[len]);
		len--;
	}
	return (NULL);
}
